function pythonTopics(moduleId: string, topicTitles: string[]) {
  return topicTitles.map((title, index) => ({
    id: `${moduleId}-topic-${index + 1}`,
    moduleId,
    title,
    difficulty:
      index <= 1
        ? ("beginner" as const)
        : index <= 3
          ? ("intermediate" as const)
          : ("advanced" as const),
    prerequisites: index === 0 ? [] : [`${moduleId}-topic-${index}`],
  }));
}

